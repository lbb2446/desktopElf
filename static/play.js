        function getPath(model, name) {
          model=JSON.parse(model)
          const path = "assets/" + name + "/"
          if (model.textures != undefined) {
          model.textures.forEach((txt, index) => {
            model.textures[index] = path + txt
          });
        }
          if (model.expressions != undefined) {
            model.expressions.forEach((exp, index) => {
              model.expressions[index].file = path + exp.file
            })
          }
  
          model.model = path + model.model
          Object.keys(model.motions).forEach((m) => {
            model.motions[m][0].file = path + model.motions[m][0].file
          })
          return model
        }
  
